package javaapplication1;

/**
 * This enumerated type models the 3 types of repetition in a repeat appointment.
 * Daily repeats everyday.
 * Weekly repeats on the same weekday.
 * Yearly repeats on the same month and day.
 * @author K. Hui
 */
public enum RepeatType {
    DAILY, WEEKLY, YEARLY;
} //end enum
